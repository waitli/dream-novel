/**
 * 前文摘要更新提示词（结构化 + 防截断）
 * 采用分段式存储，避免截断问题
 */
export const summary = (params) => `
以下是新完成的章节文本：
${params.chapterText}

这是当前的前文摘要：
${params.globalSummary || '(无)'}

请根据本章新增内容，更新前文摘要。

## 摘要结构（必须严格按照以下格式）

### 【核心主线】(800 字)
- 故事核心冲突和主角目标
- 当前进展阶段（详细）
- 主要对手/障碍及其动机
- 核心矛盾的演变过程

### 【重要事件】(3000 字)
按时间顺序详细列出关键事件：
1. 第 1-10 章：[重大事件 3-5 个，每个 150-200 字]
2. 第 11-20 章：[重大事件 3-5 个，每个 150-200 字]
3. 第 21-30 章：[重大事件 3-5 个，每个 150-200 字]
...
N. 第 X-当前章：[最新事件详细]

### 【角色关系】(1500 字)
- 主角与关键角色的关系变化（详细）
- 重要阵营/势力的立场及变化
- 未解决的关系冲突及其原因
- 角色间的羁绊和张力来源

### 【未解之谜】(1500 字)
- 已埋设但未回收的伏笔（详细列表）
- 悬念和待揭示的真相
- 读者期待解答的问题
- 每个伏笔的埋设章节和当前状态

### 【世界观揭示】(1500 字)
- 已揭示的世界规则（详细）
- 特殊能力/物品设定及其限制
- 地理/历史背景
- 社会结构和文化氛围

### 【时间线梳理】(700 字)
- 主要事件的时间顺序
- 倒计时状态（如有）
- 角色年龄/修炼时长等时间相关信息

## 更新要求
1. **总字数控制在 9000 字以内**（各部分按比例分配）
2. 保留既有重要信息，同时融入新剧情要点
3. 以简洁、连贯的语言描述
4. 客观描绘，不展开联想或解释
5. **如果超过字数限制，优先保留：核心主线 > 重要事件 > 角色关系 > 未解之谜 > 世界观揭示 > 时间线梳理**
6. 对于久远的内容（如 50 章前），可以适度精简，但不得删除关键伏笔和核心事件
7. 每个重大事件必须包含：起因、经过、结果、影响

仅返回前文摘要文本，不要解释任何内容。
`

/**
 * 角色状态更新提示词（结构化 + 防截断）
 * 采用表格化存储，节省空间
 */
export const updateCharacterState = (params) => `
以下是新完成的章节文本：
${params.chapterText}

这是当前的角色状态文档：
${params.oldState}

请更新主要角色状态，保持原有格式结构。

## 角色状态格式（必须严格按照以下格式）

### 【主角】(300 字/人)
[角色名] | 状态：[活跃/离场/死亡] | 章节：[初登场→当前]
- 物品：[关键物品 3-5 个，每件 15 字，包含来源和用途]
- 能力：[核心能力 3-5 个，每个 20 字，包含等级和限制]
- 状态：[身体状态 30 字 | 心理状态 50 字]
- 关系：[关键关系 3-5 个，每个 20 字，包含关系性质和变化]
- 目标：[当前目标 50 字，包含短期和长期目标]
- 变化：[本章变化 50 字，详细描述变化过程]
- 成长轨迹：[从初始到当前的成长历程 50 字]

### 【重要配角】(200 字/人)
[角色名] | 状态：[活跃/离场/死亡] | 章节：[初登场→最后出现]
- 身份：[角色身份 30 字，包含背景和立场]
- 关系：[与主角关系 30 字，包含关系演变]
- 状态：[当前状态 30 字，包含身体和心理]
- 目标：[个人目标 30 字]
- 变化：[本章变化 40 字，详细描述]
- 核心动机：[驱动角色行为的深层原因 40 字]

### 【次要角色】(100 字/人)
[角色名] | 状态：[活跃/离场] | 最后出现：[章节]
- 身份：[20 字]
- 关系：[与主角关系 20 字]
- 作用：[在剧情中的作用 30 字]
- 状态：[当前状态 30 字]

### 【角色关系网】(500 字)
用箭头表示关系变化：
- [角色 A] → [信任] → [角色 B]（第 X 章建立，第 Y 章加深，原因...）
- [角色 C] → [敌对] → [角色 D]（第 X 章激化，原因...）
- [角色 E] → [暧昧] → [角色 F]（第 X 章萌芽，当前状态...）
- ...（详细列出所有重要关系）

### 【阵营/势力关系】(300 字)
- [阵营 A] ↔ [阵营 B]：[关系性质，合作/敌对/中立，演变过程]
- [阵营 C] ↔ [阵营 D]：[关系性质，关键事件影响]
- ...

### 【淡出角色】(200 字)
详细列出已淡出视线的角色：
- [角色名]：第 X 章后未出现，原因 [详细]，可能回归章节 [预测]
- [角色名]：第 X 章后未出现，原因 [详细]，可能回归章节 [预测]
- ...

## 更新要求
1. **总字数控制在 5000 字以内**
2. 优先保留：主角 > 重要配角 > 关系网 > 次要角色 > 淡出角色
3. 对于 30 章以上未出现的角色，移入【淡出角色】，但保留详细信息
4. 新出场角色简要描述即可（100 字以内），后续章节再补充
5. 关系变化必须标注章节和原因
6. 语言简洁但信息完整，使用关键词和短句结合

仅返回更新后的角色状态文本，不要解释任何内容。
`

/**
 * 伏笔追踪更新提示词（新增功能）
 * 专门追踪伏笔的埋设和回收
 */
export const trackForeshadowing = (params) => `
以下是新完成的章节文本：
${params.chapterText}

这是当前的伏笔追踪列表：
${params.foreshadowingList || '(无)'}

请分析本章内容，更新伏笔追踪列表。

## 伏笔格式

### 【新增伏笔】
- [伏笔名称]：第 X 章埋设，预计第 Y 章回收，描述 [详细，50-80 字]，类型 [物品/关系/身份/事件]

### 【强化伏笔】
- [伏笔名称]：第 X 章埋设，第 Y 章再次提及，线索加深 [详细描述 50 字]

### 【回收伏笔】
- [伏笔名称]：第 X 章埋设 → 第 Y 章回收，效果 [详细描述回收效果和读者反馈预期 80 字]

### 【待回收伏笔】(按优先级排序)
1. [高优先级] [伏笔名称]：第 X 章埋设，已超过 Y 章未回收，描述 [30 字]
2. [中优先级] [伏笔名称]：第 X 章埋设，预计第 Z 章回收，描述 [30 字]
3. [低优先级] [伏笔名称]：第 X 章埋设，刚埋设不久，描述 [30 字]

## 更新要求
1. 清晰标注伏笔的埋设章节和回收章节
2. 回收的伏笔移入【回收伏笔】，从【待回收】中移除
3. 超过 30 章未回收的伏笔标记为 [高优先级]
4. 每个伏笔描述详细但精炼（50-80 字）
5. 标注伏笔类型（物品/关系/身份/事件/世界观）

仅返回更新后的伏笔追踪列表，不要解释任何内容。
`

/**
 * 导出所有工具类提示词
 */
export const utilityPrompts = {
  summary,
  updateCharacterState,
  trackForeshadowing
}
